async function submitReport() {
    const code = document.getElementById('reportCode').value.trim();
    const note = document.getElementById('reportNote').value.trim();
    const t = i18n[currentLang] || i18n['en'];

    if (!code) return;

    // Change this to your Formspree ID (e.g., https://formspree.io/f/mqkvnrvd)
    const FORMSPREE_ENDPOINT = "https://formspree.io/f/mjglkbrw";

    reportSubmitBtn.disabled = true;
    const originalContent = reportSubmitBtn.innerHTML;
    reportSubmitBtn.innerHTML = '<i class="fa-solid fa-circle-notch fa-spin"></i>';

    try {
        const response = await fetch(FORMSPREE_ENDPOINT, {
            method: 'POST',
            headers: {
                'Accept': 'application/json',
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({
                errorCode: code,
                description: note,
                language: currentLang,
                userAgent: navigator.userAgent
            })
        });

        if (response.ok) {
            alert(t.reportSuccess);
            closeReportModal();
            document.getElementById('reportCode').value = '';
            document.getElementById('reportNote').value = '';
        } else {
            throw new Error('Network response was not ok.');
        }
    } catch (error) {
        console.error('Error submitting form:', error);
        alert(currentLang === 'cs' ? 'Chyba při odesílání. Zkuste to prosím později.' : 'Error submitting. Please try again later.');
    } finally {
        reportSubmitBtn.disabled = false;
        reportSubmitBtn.innerHTML = originalContent;
    }
}

