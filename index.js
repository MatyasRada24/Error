/**
 * Share button handler – uses Web Share API on mobile/supported browsers,
 * falls back to clipboard copy on desktop.
 */
function shareError() {
    const shareBtn = document.getElementById('shareBtn');
    const url = shareBtn?.dataset.shareUrl || window.location.href;
    const title = shareBtn?.dataset.shareTitle || 'Error Code Explorer';

    if (navigator.share) {
        navigator.share({
            title: `${title} – Error Code Explorer`,
            text: `Řešení chybového kódu ${title} na Error Code Explorer`,
            url
        }).catch(() => { /* user cancelled */ });
    } else {
        // Fallback: copy link to clipboard
        navigator.clipboard.writeText(url).then(() => {
            if (shareBtn) {
                shareBtn.innerHTML = '<i class="fa-solid fa-check"></i>';
                shareBtn.classList.add('copied');
                setTimeout(() => {
                    shareBtn.innerHTML = '<i class="fa-solid fa-share-nodes" aria-hidden="true"></i>';
                    shareBtn.classList.remove('copied');
                }, 2500);
            }
        }).catch(() => {
            // Last resort: select input trick
            const tmp = document.createElement('input');
            tmp.value = url;
            document.body.appendChild(tmp);
            tmp.select();
            document.execCommand('copy');
            document.body.removeChild(tmp);
        });
    }
}

