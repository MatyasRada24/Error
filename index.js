function highlightText(text, query) {
    if (!query) return text;
    const regex = new RegExp(`(${query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')})`, 'gi');
    return text.replace(regex, '<mark class="highlight">$1</mark>');
}

function buildCard(err, t) {
    const query = searchInput.value.trim();
    const card = document.createElement('div');
    card.className = `card ${err.type}`;
    const cat = err.category[currentLang] || err.category['en'] || '';

    // Truncate description for better card layout
    let desc = err.description[currentLang] || err.description['en'] || '';
    const MAX_DESC = 140;
    if (desc.length > MAX_DESC) desc = desc.substring(0, MAX_DESC) + '...';

    // Highlight search term
    const highlightedTitle = highlightText(err.code, query);
    const highlightedDesc = highlightText(desc, query);

    // Extract first step or truncate solution
    let solRaw = err.solution[currentLang] || err.solution['en'] || '';
    let solSnippet = solRaw;
    if (solRaw.startsWith('<ol')) {
        // Extract and strip HTML from first <li> item (dotAll for multiline)
        const match = solRaw.match(/<li>([\s\S]*?)<\/li>/);
        solSnippet = match ? match[1].replace(/<[^>]*>/g, '').trim() : '';
    } else {
        solSnippet = solRaw.replace(/<[^>]*>/g, '').trim();
    }
    const MAX_SOL = 200;
    if (solSnippet.length > MAX_SOL) solSnippet = solSnippet.substring(0, MAX_SOL) + '...';

    let vHtml = '';
    if (err.vendors) vHtml = `<div class="vendor-chips">${err.vendors.map(v => `<span class="vendor-chip ${v}">${v}</span>`).join('')}</div>`;

    card.innerHTML = `
        <div class="card-header">
            <span class="badge ${err.type}">${err.type.toUpperCase()}</span>
            <span class="category">${cat}</span>
        </div>
        <h3 class="code-title"><a href="error/${err.id}/" class="card-title-link" onclick="event.preventDefault(); window.openById('${err.id}');">${highlightedTitle}</a></h3>
        <div class="card-section">
            <p class="description"><strong>${t.modalDescTitle}:</strong> ${highlightedDesc}</p>
        </div>
        ${vHtml}
        <div class="solution">
            <h4><i class="fa-solid fa-wrench"></i> ${t.solutionCardTitle}</h4>
            <p>${solSnippet}</p>
        </div>
        <a href="error/${err.id}/" class="card-detail-link" onclick="event.preventDefault(); window.openById('${err.id}');">${t.cardHint} →</a>
    `;
    card.addEventListener('click', () => openModal(err));

    // Interactive mouse hover glow effect
    card.addEventListener('mousemove', e => {
        const rect = card.getBoundingClientRect();
        const x = e.clientX - rect.left;
        const y = e.clientY - rect.top;
        card.style.setProperty('--mouse-x', `${x}px`);
        card.style.setProperty('--mouse-y', `${y}px`);
    });

    return card;
}

function renderCards() {
    const query = searchInput.value.toLowerCase().trim();

    const filtered = errorCodes.filter(err => {
        if (activeType !== 'all' && err.type !== activeType) return false;
        if (activeSub !== 'all' && err.subcategory !== activeSub) return false;
        if (query) {
            const s = err._search;
            return s.code.includes(query) || s.id.includes(query) || s[currentLang].includes(query);
        }
        return true;
    });

    updateVendorTabs(filtered);

    const final = filtered.filter(err => {
        if (activeVendor !== 'all' && (!err.vendors || !err.vendors.includes(activeVendor))) return false;
        return true;
    });

    currentFinalList = final;
    renderedCount = 0;

    // Remove old scroll listener
    if (scrollListenerActive) {
        window.removeEventListener('scroll', onScrollLoadMore, { passive: true });
        scrollListenerActive = false;
    }

    if (final.length === 0) {
        noResultsDiv.classList.remove('hidden');
        requestAnimationFrame(() => {
            resultsContainer.innerHTML = '';
            if (ioObserver) { ioObserver.disconnect(); ioObserver = null; }
            if (ioSentinel && ioSentinel.parentNode) ioSentinel.remove();
        });
    } else {
        noResultsDiv.classList.add('hidden');
        // If it's a new search, show skeletons first
        if (renderedCount === 0) {
            resultsContainer.innerHTML = document.getElementById('skeletons').innerHTML;
        }

        // Use requestAnimationFrame to ensure the browser has a chance to paint the skeletons
        // then swap for real cards in the next frame
        requestAnimationFrame(() => {
            if (renderedCount === 0) resultsContainer.innerHTML = '';
            loadMoreCards();
            if (currentFinalList.length > renderedCount) {
                setupInfiniteScroll();
            }
        });
    }


    // Sync URL to current search/filter state (no code param – modal is closed)
    updateURL({
        q: searchInput.value.trim() || null,
        lang: currentLang,
        type: activeType
    });
}

function updateVendorTabs(data) {
    const vendors = new Set();
    data.forEach(err => { if (err.vendors) err.vendors.forEach(v => vendors.add(v)) });

    // Hide tags if 'All' is selected in subtabs (or main tab)
    if (vendors.size > 0 && activeSub !== 'all') {
        const t = i18n[currentLang] || i18n['en'];
        vendorTabs.classList.remove('hidden');
        let html = `<button class="vendor-tab ${activeVendor === 'all' ? 'active' : ''}" data-vendor="all">${t.vendorAll}</button>`;
        Array.from(vendors).sort().forEach(v => {
            html += `<button class="vendor-tab ${activeVendor === v ? 'active' : ''}" data-vendor="${v}">${v.toUpperCase()}</button>`;
        });
        vendorTabs.innerHTML = html;
        vendorTabs.querySelectorAll('.vendor-tab').forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.stopPropagation();
                activeVendor = btn.dataset.vendor;
                renderCards();
            });
        });
    } else {
        vendorTabs.classList.add('hidden');
        activeVendor = 'all';
    }
}

