/**
 * Build a clean URL string reflecting current app state.
 * Omits default values to keep URLs short.
 */
function buildURL({ code = null, q = null, lang = null, type = null } = {}) {
    const url = new URL(window.location.origin + window.location.pathname);
    if (code) url.searchParams.set('code', code);
    if (q) url.searchParams.set('q', q);
    if (lang && lang !== 'cs') url.searchParams.set('lang', lang);
    if (type && type !== 'all') url.searchParams.set('type', type);
    return url.toString();
}

/** Replace the current history entry without causing a page reload. */
function updateURL(params = {}) {
    try {
        history.replaceState({ ...params }, '', buildURL(params));
    } catch (e) { /* silently skip if History API unavailable */ }
}

/**
 * Return a canonical shareable URL for a specific error ID.
 * Uses /error/{id}/ SEO-friendly path, e.g. https://errorsfixer.com/error/gpu-43/
 */
function getShareURL(errorId) {
    let base = `${window.location.origin}/error/${errorId}/`;
    return base;
}

/**
 * Read URL parameters on page load and apply them to app state
 * before the first render. Called once just before updateUI().
 * Handles both /error/{id}/ path URLs and legacy ?code= query params.
 */
function readURLParams() {
    const params = new URLSearchParams(window.location.search);

    // ?lang=en / ?lang=zh
    const lang = params.get('lang');
    if (lang && ['cs', 'en', 'zh'].includes(lang)) {
        currentLang = lang;
        const opt = document.querySelector(`.lang-option[data-value="${lang}"]`);
        if (opt) {
            document.querySelectorAll('.lang-option').forEach(o => o.classList.toggle('active', o === opt));
            const ltxt = document.getElementById('currentLangText');
            if (ltxt) ltxt.textContent = opt.textContent;
        }
    }

    // ?type=hardware / ?type=software
    const type = params.get('type');
    if (type && ['hardware', 'software'].includes(type)) {
        activeType = type;
        document.querySelectorAll('.tab').forEach(b =>
            b.classList.toggle('active', b.dataset.type === type)
        );
        hwSubTabs.classList.toggle('hidden', type !== 'hardware');
        swSubTabs.classList.toggle('hidden', type !== 'software');
        filterToggle.classList.remove('hidden');
    }

    // ?q=bsod  (pre-fill search box)
    const q = params.get('q');
    if (q) {
        searchInput.value = q;
        clearSearchBtn.classList.remove('hidden');
    }

    // ── Deep link: /error/{id}/ path (canonical SEO URL) ──────────────────
    const pathMatch = window.location.pathname.match(/^\/error\/([^\/]+)\/?$/);
    if (pathMatch) {
        window._pendingDeepLinkCode = pathMatch[1];
        // Silently rewrite history to SPA root so back button works correctly
        try { history.replaceState({ code: pathMatch[1] }, '', window.location.href); } catch (e) { }
        return; // skip legacy ?code= when we have a path
    }

    // ?code=gpu-43  (legacy deep link – still supported)
    const code = params.get('code');
    if (code) window._pendingDeepLinkCode = code;
}

